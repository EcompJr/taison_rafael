      <p class="text-justify">A Fundação Jonathas Telles de Carvalho é uma Entidade Filantrópica, sem fins lucrativos, criada pelos Clubes de Rotary
        da Cidade de Feira de Santana – Ba. Com o objetivo de Servir à Humanidade e fazer valer o Lema de Rotary Internacional:
        Dar de Si, antes de Pensar em Si. Assim sendo, fora idealizada para dar assistência às pessoas portadoras de deficiência
        visual: cegas ou com visão subnormal, pela carência de serviços e recursos na área educacional que pudessem atender
        às necessidades mais básicas dessas pessoas, dando origem ao seu Centro de Apoio Pedagógico ao Deficiente Visual
        – pioneiro no Interior da Bahia.</p>
      <p class="text-justify">
        Visto as dificuldades de se manter uma Instituição Especializada, com a modalidade de Ensino Especial, referindo-se à contratação
        de Recursos Humanos Especializados e à aquisição de equipamentos e materiais didáticos específicos, utilizados na
        educação e reabilitação da pessoa com deficiência visual, com valores considerados exorbitantes, a Fundação Jonathas
        Telles de Carvalho, ciente de que, sozinha não seria capaz de levar adiante, um trabalho de qualidade, que vem se
        expandindo a cada ano, para somar aos esforços despreendidos pela Secretaria Municipal de Educação, a qual, ao longo
        desses anos, vem mantendo Convênio com nossa Instituição: “Serviços em Educação Especial”, firmou Convênio também
        com a Secretaria Estadual de Educação – “Cessão de Salas” , nº 30/2005, publicado no Diário Oficial do Estado da
        Bahia, do dia 24 de março de 2005, a qual, por sua vez, criou oficialmente como Unidade Escolar, o Centro de Apoio
        Pedagógico ao Deficiente Visual, Código 1178163, através da Portaria nº 6848, sendo considerados esses Convênios,
        conquistas sólidas, os quais são responsáveis pela melhoria dos serviços prestados à humanidade , dando maior credibilidade
        e reconhecimento ao Centro de Apoio, reconhecido como referência, na área da deficiência visual, não só em nossa
        cidade, como também, em outras cidades da região.
      </p>
      <p class="text-justify">
        Segundo o MEC, “cabe à Educação Especial oferecer oportunidades que garantam o acesso e a permanência na escola do aluno
        portador de necessidades educativas especiais, bem como a sua terminalidade acadêmica.” Assim sendo, vale ressaltar
        aqui, a importância dos dois Convênios para a Fundação Jonathas Telles de Carvalho, considerando a heterogeneidade
        do alunado e seu fluxo de escolarização (da Educação Infantil ao Ensino Superior), exigindo um atendimento especial,
        otimizado , com metodologias e recursos didáticos próprios. Os Programas desenvolvidos pelo Centro de Apoio Pedagógico
        ao Deficiente Visual, têm servido de suporte para a escolarização dos alunos deficientes visuais e como orientação
        à prática dos educadores das salas de aula, onde estão inclusos esses alunos, quer das Escolas da Rede Municipal,
        ou da Rede Estadual, ou até mesmo da Rede Privada. Um outro fator a ser considerado, é o momento histórico que estamos
        vivenciando: a Municipalização de Ensino, de grande relevância para as duas esferas de Governo, o que aumenta a nossa
        responsabilidade, enquanto Instituição, a qual precisa, para facilitar o processo de inclusão - com respaldo filosófico,
        legal e político-educacional em muitos documentos da Legislação Brasileira e Mundial - manter uma política não partidária,
        priorizando acima de tudo, o lado humano das pessoas e seus direitos enquanto cidadãos, para poder continuar funcionando
        como apoio aos Governos, constituindo-se em grandes parceiros no cumprimento constitucional da Educação para todos,
        livre de barreiras, principalmente, atitudinais, pois a Educação Inclusiva, tendência internacional, deve ser uma
        meta a ser perseguida por todos aqueles comprometidos com a Educação Especial.
      </p>
      <p class="text-justify">
        Podemos dizer que, a soma e a conquista de grandes parceiros, viabilizará a implementação de ações e projetos futuros, os
